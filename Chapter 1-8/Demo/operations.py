# integers
print("Integer operations:")
a = 10
b = 4
c = 0
print(f"a+b = {a+b}")
print(f"a-b = {a-b}")
print(f"a*b = {a*b}")
print(f"a/b = {a/b}")
print(f"a**b = {a**b}")
print(f"a//b = {a//b}")
print(f"a % b = {a % b}")
print(f"a and b = {a and b}")  # returns smallest of the the two operands
print(f"a and c = {a and c}")  # returns smallest
print(f"a or c = {a or c}")   # returns largest of the two operands
print(f"a < b = {a < b}")
print(f"a <= b = {a <= b}")
print(f"a > b = {a > b}")
print(f"a >= b = {a >= b}")
print(f"a != b = {a != b}")
print(f"a == b = {a == b}")
a += b
print(f"a += b -> {a}")
a -= b
print(f"a -= b -> {a}")
a *= b
print(f"a *= b -> {a}")
a //= b  # 'a' stays int
print(f"a //= b -> {a}")
a *= b
print(f"a *= b -> {a}")
a **= 2
print(f"a **= 2 -> {a}")
a %= 17
print(f"a %= 17 -> {a}")
a /= b  # 'a' becomes float
print(f"a /= b -> {a}")

